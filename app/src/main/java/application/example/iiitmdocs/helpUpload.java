package application.example.iiitmdocs;

public class helpUpload {

    public String name;
    public String url;

    public helpUpload() {
    }

    public helpUpload(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }
}
